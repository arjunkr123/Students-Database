function InformationSession(id, name, event_id, geographic_area_id, number_boys_expected, number_girls_expected, number_boys_real, number_girls_real) {
	this.id = id;
	this.name = name;
	this.event_id = event_id;
	this.geographic_area_id = geographic_area_id;
	this.number_boys_expected = number_boys_expected;
	this.number_girls_expected = number_girls_expected;
	this.number_boys_real = number_boys_real;
	this.number_girls_real = number_girls_real;
}